/**
 * Resumo: Este código mostra como trabalhar com números, incluindo inteiros, pontos flutuantes e valores especiais como Infinity e NaN.
 * Uso prático: Use números para cálculos matemáticos, contagens, ou qualquer operação que envolva valores numéricos.
 */

// Número inteiro
let idade = 25;
console.log(idade); // 25

// Número de ponto flutuante
let preco = 19.99;
console.log(preco); // 19.99

// Número negativo
let temperatura = -10;
console.log(temperatura); // -10

// Infinity
let infinito = Infinity;
console.log(infinito); // Infinity

// NaN (Not-a-Number)
let resultado = 0 / 0;
console.log(resultado); // NaN

// Operações matemáticas
let soma = idade + preco;
console.log(soma); // 44.99