/*
Descrição:
IIFE (Immediately Invoked Function Expression) é uma função que é 
executada imediatamente após ser definida. É útil para criar escopos isolados.
*/

// Definindo e executando uma IIFE
(function() {
    console.log("IIFE executada!"); // Exibe: "IIFE executada!"
})(); // Os parênteses no final fazem a função ser executada imediatamente