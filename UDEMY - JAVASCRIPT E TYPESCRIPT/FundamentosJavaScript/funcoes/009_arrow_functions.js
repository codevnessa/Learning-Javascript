/*
Descrição:
Arrow functions são uma forma moderna e curta de escrever funções. 
Elas usam a sintaxe "=>" e são úteis para funções simples.
*/

// Definindo uma arrow function que retorna a raiz quadrada de "n"
const raiz = n => n ** 0.5;

// Chamando a arrow function
console.log(raiz(16)); // Resultado: 4 será exibido no console