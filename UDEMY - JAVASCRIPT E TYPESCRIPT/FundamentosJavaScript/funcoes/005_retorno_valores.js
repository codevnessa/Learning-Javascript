/*
Descrição:
Uma função pode retornar um valor usando a palavra-chave "return". 
Esse valor pode ser usado em outras partes do código.
*/

// Definindo uma função chamada "soma" que retorna a soma de dois números
function soma(x, y) {
    return x + y; // Retorna o resultado da soma
}

// Chamando a função e armazenando o valor retornado
let resultado = soma(5, 3); // O valor retornado (8) é armazenado na variável "resultado"
console.log(resultado); // Resultado: 8 será exibido no console