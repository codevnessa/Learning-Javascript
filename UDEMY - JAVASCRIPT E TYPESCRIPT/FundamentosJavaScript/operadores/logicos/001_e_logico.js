// Operador: && (E lógico)
// Descrição: Retorna true se todas as condições forem verdadeiras.
// Exemplo: 10 > 5 && 5 < 10

let condicao1 = 10 > 5;
let condicao2 = 5 < 10;
let resultado = condicao1 && condicao2;

console.log(`Ambas as condições são verdadeiras? ${resultado}`);
// Explicação: Como ambas as condições são verdadeiras, o resultado será true.