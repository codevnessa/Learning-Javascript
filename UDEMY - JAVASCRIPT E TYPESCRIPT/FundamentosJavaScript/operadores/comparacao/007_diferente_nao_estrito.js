// Operador: Diferente (não estrito) (!=)
// Descrição: Verifica se os valores são diferentes, sem considerar o tipo de dado.
// Exemplo: 10 != "10"

let a = 10;
let b = "10";
let resultado = a != b;

console.log(`O resultado de ${a} != "${b}" é: ${resultado}`);
// Explicação: Como o valor é o mesmo, mesmo que o tipo seja diferente, o resultado será false.