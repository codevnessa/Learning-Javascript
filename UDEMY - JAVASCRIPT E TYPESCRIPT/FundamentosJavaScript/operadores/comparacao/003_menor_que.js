// Operador: Menor que (<)
// Descrição: Verifica se o valor à esquerda do operador é menor que o valor à direita.
// Exemplo: 5 < 10

let a = 5;
let b = 10;
let resultado = a < b;

console.log(`O resultado de ${a} < ${b} é: ${resultado}`);
// Explicação: Como 5 é menor que 10, o resultado será true.