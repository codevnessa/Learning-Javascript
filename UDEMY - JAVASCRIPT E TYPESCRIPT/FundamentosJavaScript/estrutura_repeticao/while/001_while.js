/*

O loop 'while' executa um bloco de código enquanto uma condição especificada for verdadeira. 
A condição é avaliada antes de cada iteração, e o loop só continua se a condição for verdadeira. 
Se a condição for falsa desde o início, o bloco de código nunca será executado.

*/ 

let contador = 0;
while (contador < 3) {
    console.log(`Contador: ${contador}`);
    contador++;
}

/*

Neste exemplo, o loop 'while' é utilizado para iterar enquanto o valor de 'contador' for menor que 3. 
A cada iteração, o valor de 'contador' é incrementado em 1.

*/