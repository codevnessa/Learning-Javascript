/*
O loop 'for' é uma estrutura de controle que permite repetir um bloco de código um número específico de vezes. 
Ele é composto por três partes: inicialização, condição e incremento/decremento. 
A inicialização é executada uma vez no início do loop, a condição é verificada antes de cada iteração, 
e o incremento/decremento é executado após cada iteração.
*/

// Exemplo de uso do 'for'
for (let i = 0; i < 5; i++) {
    console.log(`Iteração número: ${i}`);
}

/*
Neste exemplo, o loop 'for' é utilizado para iterar 5 vezes. 
A variável 'i' é inicializada com 0, e a condição 'i < 5' é verificada antes de cada iteração. 
Após cada iteração, o valor de 'i' é incrementado em 1. 
*/