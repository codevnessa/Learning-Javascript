/*
O loop 'for...of' é utilizado para iterar sobre elementos de objetos iteráveis, como arrays, strings, mapas, conjuntos, etc. 
Ele percorre cada elemento do iterável, permitindo acessar diretamente o valor do elemento.
*/

// Exemplo de uso do 'for...of'
const frutas = ['maçã', 'banana', 'laranja'];

for (let fruta of frutas) {
    console.log(fruta);
}

/*
Neste exemplo, o loop 'for...of' é utilizado para iterar sobre os elementos do array 'frutas'. 
A variável 'fruta' assume o valor de cada elemento do array a cada iteração. 
*/