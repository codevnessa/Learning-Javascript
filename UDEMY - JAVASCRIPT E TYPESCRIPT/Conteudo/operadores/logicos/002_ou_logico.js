// Operador: || (OU lógico)
// Descrição: Retorna true se pelo menos uma condição for verdadeira.
// Exemplo: 10 > 5 || 5 > 10

let condicao1 = 10 > 5;
let condicao2 = 5 > 10;
let resultado = condicao1 || condicao2;

console.log(`Pelo menos uma condição é verdadeira? ${resultado}`);
// Explicação: Como a primeira condição é verdadeira, o resultado será true.