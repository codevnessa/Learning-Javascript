// Operador: ! (NÃO lógico)
// Descrição: Inverte o valor booleano de uma condição.
// Exemplo: !(10 > 5)

let condicao = 10 > 5;
let resultado = !condicao;

console.log(`O inverso da condição "10 > 5" é: ${resultado}`);
// Explicação: Como a condição é verdadeira, o operador ! a inverte para false.