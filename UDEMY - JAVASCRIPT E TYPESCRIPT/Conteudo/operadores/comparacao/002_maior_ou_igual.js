// Operador: Maior ou igual a (>=)
// Descrição: Verifica se o valor à esquerda do operador é maior ou igual ao valor à direita.
// Exemplo: 10 >= 10

let a = 10;
let b = 10;
let resultado = a >= b;

console.log(`O resultado de ${a} >= ${b} é: ${resultado}`);
// Explicação: Como 10 é igual a 10, o resultado será true.