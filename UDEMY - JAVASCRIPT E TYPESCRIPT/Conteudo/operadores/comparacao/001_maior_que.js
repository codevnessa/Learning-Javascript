// Operador: Maior que (>)
// Descrição: Verifica se o valor à esquerda do operador é maior que o valor à direita.
// Exemplo: 10 > 5

let a = 10;
let b = 5;
let resultado = a > b;

console.log(`O resultado de ${a} > ${b} é: ${resultado}`);
// Explicação: Como 10 é maior que 5, o resultado será true.