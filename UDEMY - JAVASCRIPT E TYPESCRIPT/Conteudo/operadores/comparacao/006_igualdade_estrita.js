// Operador: Igualdade estrita (===)
// Descrição: Verifica se os valores e os tipos de dados são iguais.
// Exemplo: 10 === "10"

let a = 10;
let b = "10";
let resultado = a === b;

console.log(`O resultado de ${a} === "${b}" é: ${resultado}`);
// Explicação: Como o tipo de dado é diferente (número vs string), o resultado será false.