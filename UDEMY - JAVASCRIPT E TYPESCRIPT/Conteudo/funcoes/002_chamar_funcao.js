/*
Descrição:
Para executar uma função, basta chamá-la pelo nome seguido de parênteses.
Isso faz com que o código dentro da função seja executado.
*/

// Definindo uma função chamada "saudacao"
function saudacao() {
    console.log("Bom dia!"); // Exibe a mensagem "Bom dia!" no console
}

// Chamando a função para executar o código dentro dela
saudacao(); // Resultado: "Bom dia!" será exibido no console