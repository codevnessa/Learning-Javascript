/*
Descrição:
Em JavaScript, funções podem ser armazenadas em variáveis, passadas como 
argumentos e retornadas por outras funções.
*/

// Armazenando uma função na variável "minhaFuncao"
const minhaFuncao = function() {
    console.log("Olá!"); // Exibe "Olá!" quando a função é chamada
};

// Chamando a função armazenada na variável
minhaFuncao(); // Resultado: "Olá!" será exibido no console