/*
Descrição:
Uma função é um bloco de código que realiza uma tarefa específica. 
Ela pode ser chamada quantas vezes você quiser, evitando repetição de código.
*/

// Definindo uma função chamada "saudacao"
function saudacao() {
    // Dentro da função, escrevemos o código que queremos executar
    console.log("Bom dia!"); // Exibe a mensagem "Bom dia!" no console
}

// Chamando a função para executar o código dentro dela
saudacao(); // Resultado: "Bom dia!" será exibido no console